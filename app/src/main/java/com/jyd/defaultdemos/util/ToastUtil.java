public class ToastUtil {

    public static void show(CharSequence text) {
        if (text.length() < 15) {
            Toast.makeText(App.getInstance(), text, Toast.LENGTH_SHORT).show();
        } else {
            Toast.makeText(App.getInstance(), text, Toast.LENGTH_LONG).show();
        }
    }
}
